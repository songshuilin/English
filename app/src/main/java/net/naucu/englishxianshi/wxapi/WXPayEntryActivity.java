public class WXPayEntryActivity extends Activity implements IWXAPIEventHandler {
    public static final String TAG = WXPayEntryActivity.class.getSimpleName();

    private IWXAPI api;

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        api = WXAPIFactory.createWXAPI(this, Constants.APP_ID);
        api.handleIntent(getIntent(), this);
    }

    @Override
    protected void onNewIntent(Intent intent) {
        super.onNewIntent(intent);
        setIntent(intent);
        api.handleIntent(intent, this);
    }

    @Override
    public void onReq(BaseReq req) {
    }

    @Override
    public void onResp(BaseResp resp) {
        if (resp.getType() == ConstantsAPI.COMMAND_PAY_BY_WX) {
            switch (resp.errCode) {
                case 0:
                    if (PayPresenter.handler != null) {
                        PayPresenter.handler.sendEmptyMessage(PayHandler.WHAT_WEI_CHAT_SUCCESS);
                    }
                    break;
                default:
                    if (PayPresenter.handler != null) {
                        PayPresenter.handler.sendEmptyMessage(PayHandler.WHAT_WEI_CHAT_FAILURE);
                    }
                    break;
            }
            finish();
        }
    }
}